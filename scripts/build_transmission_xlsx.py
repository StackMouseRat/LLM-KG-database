def col_name(index: int) -> str:
    result = ""
    while index:
        index, remainder = divmod(index - 1, 26)
        result = chr(65 + remainder) + result
    return result


def escape_cell(value: object) -> str:
    text = "" if value is None else str(value)
    return html.escape(text, quote=False)


def build_sheet_xml(template_xml: str, rows: list[list[object]]) -> str:
    row_xml = []
    last_col = col_name(len(rows[0]))
    for row_index, row in enumerate(rows, start=1):
        cells = []
        for col_index, value in enumerate(row, start=1):
            style_attr = ' s="1"' if row_index == 1 else ""
            ref = f"{col_name(col_index)}{row_index}"
            text = escape_cell(value)
            cells.append(
                f'<c r="{ref}"{style_attr} t="inlineStr"><is><t xml:space="preserve">{text}</t></is></c>'
            )
        row_xml.append(
            f'<row r="{row_index}" spans="1:{len(row)}" x14ac:dyDescent="0.2">{"".join(cells)}</row>'
        )

    new_sheet_data = f"<sheetData>{''.join(row_xml)}</sheetData>"
    new_dimension = f'<dimension ref="A1:{last_col}{len(rows)}"/>'

    updated = re.sub(r"<dimension ref=\"[^\"]*\"/>", new_dimension, template_xml, count=1)
    updated = re.sub(r"<sheetData>.*?</sheetData>", new_sheet_data, updated, count=1, flags=re.S)
    updated = re.sub(
        r'<selection [^>]*activeCell="[^"]+"[^>]*sqref="[^"]+"[^>]*/>',
        '<selection activeCell="A1" sqref="A1"/>',
        updated,
        count=1,
    )
    return updated


def sheet_path_from_template(template_path: Path) -> str:
    with zipfile.ZipFile(template_path) as zin:
        workbook_xml = zin.read("xl/workbook.xml").decode("utf-8", errors="replace")
        rel_id = re.search(r'<sheet[^>]*r:id="([^"]+)"', workbook_xml)
        rels_xml = zin.read("xl/_rels/workbook.xml.rels").decode("utf-8", errors="replace")
        target = re.search(
            rf'<Relationship Id="{re.escape(rel_id.group(1))}"[^>]*Target="([^"]+)"',
            rels_xml,
        )
        return "xl/" + target.group(1).lstrip("/")


def write_workbook(template_path: Path, output_path: Path, rows: list[list[object]]) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    sheet_path = sheet_path_from_template(template_path)
    with zipfile.ZipFile(template_path) as zin:
        template_xml = zin.read(sheet_path).decode("utf-8", errors="replace")
        new_sheet_xml = build_sheet_xml(template_xml, rows).encode("utf-8")
        with zipfile.ZipFile(output_path, "w", compression=zipfile.ZIP_DEFLATED) as zout:
            for info in zin.infolist():
                data = zin.read(info.filename)
                if info.filename == sheet_path:
                    data = new_sheet_xml
                zout.writestr(info, data)


def build_rows() -> tuple[list[list[object]], list[list[object]]]:
    degree = Counter()
    ordered_names = []
    seen_names = set()
    for source, target, relation in RELATIONS:
        degree[source] += 1
        degree[target] += 1
        for name in (source, target):
            if name not in seen_names:
                seen_names.add(name)
                ordered_names.append(name)

    node_rows = [[
        "节点名称",
        "描述",
        "图像",
        "权重",
        "度",
        "数值",
        "横坐标",
        "纵坐标",
        "填充颜色",
        "文字颜色",
    ]]

    for index, name in enumerate(ordered_names, start=1):
        node_rows.append([
            name,
            DESCRIPTIONS.get(name, name),
            IMAGE_URL,
            category_weight(name),
            degree[name],
            index,
            "",
            "",
            "",
            "",
        ])

    link_rows = [["源节点", "目标节点", "关系名称"]]
    link_rows.extend([list(item) for item in RELATIONS])
    return node_rows, link_rows


def main() -> None:
    node_rows, link_rows = build_rows()
    write_workbook(NODES_TEMPLATE, NODES_OUTPUT, node_rows)
